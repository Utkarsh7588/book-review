const mongoose = require('mongoose');

const bookSchema = new mongoose.Schema({
  title: {
    type: String,
    required: true,
    trim: true
  },
  author: {
    type: String,
    required: true,
    trim: true
  },
  genre: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    required: true
  },
  publishedYear: {
    type: Number,
    required: true
  },
  averageRating: {
    type: Number,
    default: 0,
    min: 0,
    max: 5
  },
  totalReviews: {
    type: Number,
    default: 0
  }
}, {
  timestamps: true
});

// Setup search index
bookSchema.index({ title: 'text', author: 'text', description: 'text' });

// Create the model
const Book = mongoose.model('Book', bookSchema);

// Create indexes
Book.createIndexes().catch(console.error);

module.exports = Book; 